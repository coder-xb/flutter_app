class AreaAdapter extends PickerAdapter<String> {
  String prov, city, area; // 省/市/区
  int _col = 0, _level = -1, _pid = 0, _cid = 0, _aid = 0;
  List<dynamic> provs, cities, areas;
  List<PickerItem<String>> _datas, _rows;

  AreaAdapter({
    Picker picker,
    this.prov = '四川省',
    this.city = '成都市',
    this.area = '武侯区',
  })  : assert(prov != null && prov.isNotEmpty),
        assert(city != null && city.isNotEmpty) {
    super.picker = picker;
    _calcInit();
  }
  @override
  void init() {
    if (picker.values == null || picker.values.isEmpty) {
      if (picker.values == null) picker.values = <int>[];
      for (int i = 0; i < level; i++)
        picker.values.add(i == 0 ? _pid : (i == 1 ? _cid : _aid));
    }
  }

  @override
  int getLength() => _datas == null ? 0 : _datas.length;

  @override
  int getLevel() {
    if (_level == -1) _checkLevel(_rows, 1);
    return _level;
  }

  @override
  Widget build(BuildContext context, int index) => createText(
        child: _datas[index].child,
        text: '${_datas[index].value}',
        select: index == picker.values[_col],
      );

  @override
  void setCol(int index) {
    if (_datas != null && _col == index + 1) return;
    _col = index + 1;
    _datas = _rows;
    if (index >= 0) {
      for (int i = 0; i <= index; i++) {
        int _index = picker.values[i];
        if (_datas != null && _datas.length > _index)
          _datas = _datas[_index].children;
        else {
          _datas = null;
          break;
        }
      }
    }
  }

  @override
  List<String> getValue() {
    List<String> _value = <String>[];
    if (picker.values != null && picker.values.isNotEmpty) {
      List<PickerItem<String>> _temp = _rows;
      for (int i = 0, len = picker.values.length; i < len; i++) {
        int _index = picker.values[i];
        if (_index < 0 || _index >= _temp.length) break;
        _value.add(_temp[_index].value);
        _temp = _temp[_index].children;
        if (_temp == null || _temp.isEmpty) break;
      }
    }
    return _value;
  }

  @override
  String getText() =>
      (Set()..addAll(getValue())).toList().join().replaceAll('　', '');

  /// 计算初始数据
  void _calcInit() {
    provs = _Location.provs;
    if (_rows == null) _rows = <PickerItem<String>>[];
    for (int i = 0, pl = provs.length; i < pl; i++) {
      List<dynamic> _cities = _Location.cities(provs[i]);
      List<PickerItem<String>> __cities = <PickerItem<String>>[];
      if (provs[i] == prov) _pid = i;
      for (int j = 0, cl = _cities.length; j < cl; j++) {
        if (_cities[j]['name'] == city) _cid = j;
        if (area != null) {
          List<dynamic> _areas = _Location.areas(_cities[j]['name'], _cities);
          List<PickerItem<String>> __areas = <PickerItem<String>>[];
          for (int k = 0, al = _areas.length; k < al; k++) {
            if (_areas[k] == area) _aid = k;
            __areas.add(PickerItem<String>(value: _areas[k]));
          }
          __cities.add(
              PickerItem<String>(value: _cities[j]['name'], children: __areas));
        } else
          __cities.add(PickerItem<String>(value: _cities[j]['name']));
      }
      _rows.add(PickerItem<String>(value: provs[i], children: __cities));
    }
  }

  /// 遍历层级
  void _checkLevel(List<PickerItem> rows, int index) {
    if (rows == null && rows.isEmpty) return;
    for (int i = 0, len = rows.length; i < len; i++) {
      if (rows[i].children != null && rows[i].children.length != 0)
        _checkLevel(rows[i].children, index + 1);
    }
    if (_level < index) _level = index;
  }
}

